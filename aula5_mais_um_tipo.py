#Tipo de dado bool (Boolean)
#Ao questionar algo em um programa, só existem duas respostas, sim (True) ou não (False)
#Existem vários operadores para "Questionar"
#Como exemplo o ==, que é um operador lógico que compara se um valor é igual a outro
print(10==10) #Sim -> True (Verdadeiro)
print(10==11) #Não -> False (Falso)
print(type(10==10)) #Tipo de dado Booleano
print(type(10==11))
print(type(True)) #True também é booleano, true da erro
print(type(False)) #False também é booleano, false da erro