#Dados dos tipos Int e Fload
#Int são dados do tipo numéricoo, mas apenas números inteiros
#O tipo Int representa qualquer número
#Positivo ou negativo. Caso o número não tenha sinal, ele é positivo
print(11) #int
print(-11) #int
print(0) #int

#Float são dados numéricos com ponto flutuante (casas decimais)
#O tipo Float representa qualquer número
#Positivo ou negativo com ponto flutuante. Caso não tenha sinal, é positivo
print(1.1, 10.11) #float, sempre utilizar o PONTO para separar casas decimais, não a vírgula
print(0.0) #float
print(-1.5)#float

#A função Type mostra o tipo de dado que o Python inferiu ao valor
print(type('Lorenzo'))
print(type(1))
print(type(2.4))