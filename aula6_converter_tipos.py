# Conversão de tipos, coerção, typec convertion, typecasting, coercion
# É o ato de converter um tipo em outro
# Tipos imutáveis e primitivos: str, int, float, bool
print(1 + 1)
print(int('1')) #Caso fizer função 'type' para o tipo de (int('1')), vai dar Inteiro
# Abaixo segue exemplo de transformação de string para int na prática
print(int('1') + 1 )
# Também podemos fazer como float
print(float('1.5') + 1.5)
# Ao contrário também da
print(str(11) + 'b')